// Example of console logging a value
// console.log(5);

// Variable: can be changed
let x = 5;
console.log(x);

x = 7;
console.log(x);

// Constant: cannot be changed
const y = 10;
console.log(y);

console.log(x);

// Operator precedence: Multiplication happens before addition
console.log(7 + 3 * 10); // 7 + 30 = 37

// String concatenation
let banana = "Saya";
let mango = " hensem";
console.log(banana + mango); // Output: Saya hensem

// Concatenating numbers as strings
let one = "1";
let two = "2";
console.log(one + two); // Output: 12 (not 3, because it's string + string)
